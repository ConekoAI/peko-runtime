#[cfg(test)]
mod tests {
    use super::*;

    use tempfile::TempDir;

    fn create_test_tool(dir: &Path, name: &str, description: &str) -> PathBuf {
        let tool_dir = dir.join(name);
        std::fs::create_dir(&tool_dir).unwrap();

        let manifest = format!(
            "name: {name}\nextension_type: universal-tool\ndescription: {description}\nversion: \"1.0.0\"\nparameters:\n  type: object\n  properties:\n    input:\n      type: string\n"
        );

        let manifest_path = tool_dir.join("manifest.yaml");
        std::fs::write(&manifest_path, manifest).unwrap();

        // Create a dummy executable (script)
        let script_path = tool_dir.join(format!("{name}.py"));
        std::fs::write(&script_path, "#!/usr/bin/env python3\nprint('{}')").unwrap();

        #[cfg(unix)]
        {
            use std::os::unix::fs::PermissionsExt;
            let mut perms = std::fs::metadata(&script_path).unwrap().permissions();
            perms.set_mode(0o755);
            std::fs::set_permissions(&script_path, perms).unwrap();
        }

        tool_dir
    }

    #[test]
    fn test_universal_tool_adapter_manifest_format() {
        let adapter = UniversalToolAdapter::new();
        let format = adapter.manifest_format();

        assert!(matches!(format, ManifestFormat::Yaml { .. }));
    }

    #[tokio::test]
    async fn test_discover_tools() {
        let temp = TempDir::new().unwrap();

        create_test_tool(temp.path(), "tool1", "First tool");
        create_test_tool(temp.path(), "tool2", "Second tool");

        let adapter = UniversalToolAdapter::new();
        let tools = adapter.discover_tools(temp.path()).await;

        assert_eq!(tools.len(), 2);
        assert!(tools.iter().any(|t| t.manifest.name == "tool1"));
        assert!(tools.iter().any(|t| t.manifest.name == "tool2"));
    }

    #[tokio::test]
    async fn test_register_tool_via_unified_registry() {
        let temp = TempDir::new().unwrap();
        create_test_tool(temp.path(), "tool1", "First tool");
        create_test_tool(temp.path(), "tool2", "Second tool");

        let core = crate::extensions::framework::ExtensionCore::new();
        let adapter = UniversalToolAdapter::new();
        let tools = adapter.discover_tools(temp.path()).await;

        assert_eq!(tools.len(), 2);

        // Register both tools via the unified registry
        for tool in &tools {
            adapter.register_tool(&core, &tool.manifest).await.unwrap();
        }

        // Each tool registers 5 hooks (exec, prompt, async, status, cancel)
        // via the unified registry's atomic composite operation
        assert_eq!(core.hook_count().await, 10);
        assert_eq!(core.tool_count().await, 2);

        // Verify tool metadata is accessible
        let meta = core.get_tool_metadata("tool1").await;
        assert!(meta.is_some());
        assert_eq!(meta.unwrap().name, "tool1");
    }

    #[tokio::test]
    async fn test_load_and_register_tools() {
        let temp = TempDir::new().unwrap();
        create_test_tool(temp.path(), "tool1", "First tool");

        let core = crate::extensions::framework::ExtensionCore::new();
        let count = load_and_register_tools(&core, temp.path()).await.unwrap();

        assert_eq!(count, 1);
        assert_eq!(core.tool_count().await, 1);
    }
}
