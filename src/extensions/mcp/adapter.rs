//! MCP (Model Context Protocol) Adapter for the Extension system
//!
//! This adapter integrates MCP servers into the unified Extension Architecture.
//! MCP servers are stateful (maintain connections) and can provide tools,
//! resources, and prompts.
//!
//! # MCP Server Format
//!
//! MCP servers are configured via config files (TOML/JSON) or discovered
//! from the `mcp_servers`/ directory:
//! ```toml
//! [[servers]]
//! name = "filesystem"
//! transport = "stdio"
//! command = "npx"
//! args = ["-y", "@modelcontextprotocol/server-filesystem", "/path"]
//! auto_start = true
//! ```
//!
//! # Registration
//!
//! MCP tools are registered via the unified registry (`ExtensionCore::register_tool()`).
//! Companion hooks (prompt, async, status, cancel) are auto-generated by the
//! registry per individual tool.  This adapter only supplies the execution handler.
//!
//! Lifecycle hooks (AgentInit, AgentShutdown) are still declared via
//! `resolve_hooks()` because they are server-level, not tool-level.

