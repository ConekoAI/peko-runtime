#[async_trait]
impl HookHandler for McpToolExecuteHandler {
    async fn handle(&self, ctx: HookContext) -> HookResult {
        let (called_tool_name, _params) = match ctx.as_tool_call() {
            Some((tool_name, params, _)) => {
                let expected_prefix = format!("{}:{}", MCP_TOOL_PREFIX, self.server_name);
                if !tool_name.starts_with(&expected_prefix) {
                    return HookResult::PassThrough;
                }
                (tool_name.to_string(), params.clone())
            }
            None => return HookResult::PassThrough,
        };

        let expected_full_name = format!(
            "{}:{}:{}",
            MCP_TOOL_PREFIX, self.server_name, self.tool_name
        );
        if called_tool_name != expected_full_name {
            return HookResult::PassThrough;
        }

        debug!(
            server_name = %self.server_name,
            tool_name = %self.tool_name,
            "Executing MCP tool via Extension Framework"
        );

        let server_config = {
            let manager = self.manager.read().await;
            manager.get_server_config(&self.server_name).await
        };

        let reserved_params = server_config
            .as_ref()
            .map(|c| c.reserved_parameters.clone())
            .unwrap_or_default();

        let manager = self.manager.clone();
        let server_name = self.server_name.clone();
        let actual_tool = self.tool_name.clone();

        // F19: capture principal_id from the framework-injected
        // `ToolRuntimeContext` so tool-call-driven auto-start can hand
        // the principal's quota meter to the MCP server's sampling
        // handler. None means the call originated from a non-principal
        // scope (CLI, tests, internal auto-start) — the server gets an
        // unlimited meter.
        let calling_principal_id: Option<String> = ctx
            .get_state::<crate::extensions::framework::types::ToolRuntimeContext>("tool_context")
            .and_then(|tc| tc.principal_id.clone());

        let exec_config = peko_extension_host::ToolExecConfig::new(
            reserved_params,
            serde_json::json!({"type": "object"}),
        );

        ctx.services.async_router()
            .execute_from_hook(
                &ctx,
                &called_tool_name,
                &exec_config,
                None::<peko_extension_host::PreprocessorFn>,
                Box::new(move |p| {
                    let manager = manager.clone();
                    let server = server_name.clone();
                    let tool = actual_tool.clone();
                    let principal_id = calling_principal_id.clone();
                    Box::pin(async move {
                        // Phase 2: on-demand server start. If the MCP server is not
                        // running when the tool is invoked, attempt to start it once.
                        let needs_start = {
                            let mgr = manager.read().await;
                            match mgr.get_server_state(&server).await {
                                Ok(state) => !state.running,
                                Err(_) => true,
                            }
                        };

                        if needs_start {
                            info!(server_name = %server, "MCP server not running; starting on demand");
                            let mgr = manager.write().await;
                            // F19: tool-call-driven auto-start. The
                            // server's sampling handler will charge
                            // the calling principal's meter.
                            match mgr
                                .start_server(&server, principal_id.as_deref())
                                .await
                            {
                                Ok(()) => {
                                    info!(server_name = %server, "MCP server started on demand");
                                }
                                Err(crate::extensions::mcp::protocol::manager::ManagerError::ServerAlreadyRunning(_)) => {
                                    debug!(server_name = %server, "MCP server already running");
                                }
                                Err(e) => {
                                    return Err(anyhow::anyhow!(
                                        "MCP server '{}' could not be started for tool '{}': {}",
                                        server, tool, e
                                    ));
                                }
                            }
                        }

                        let mgr = manager.read().await;
                        let mcp_result = mgr.call_tool(&server, &tool, p).await?;
                        let json_result = serde_json::json!({
                            "success": !mcp_result.is_error,
                            "contents": mcp_result.content.iter().map(|c| match c {
                                crate::extensions::mcp::protocol::types::ToolResultContent::Text(t) => serde_json::json!({
                                    "type": "text",
                                    "text": t.text
                                }),
                                crate::extensions::mcp::protocol::types::ToolResultContent::Image(i) => serde_json::json!({
                                    "type": "image",
                                    "data": i.data,
                                    "mime_type": i.mime_type
                                }),
                                crate::extensions::mcp::protocol::types::ToolResultContent::Resource(r) => serde_json::json!({
                                    "type": "resource",
                                    "resource": r.resource
                                }),
                            }).collect::<Vec<_>>(),
                        });
                        Ok(json_result)
                    }) as futures::future::BoxFuture<'static, anyhow::Result<serde_json::Value>>
                }) as peko_extension_host::ExecFn,
            )
            .await
    }

    fn hook_point(&self) -> HookPoint {
        HookPoint::ToolExecute {
            tool_name: format!(
                "{}:{}:{}",
                MCP_TOOL_PREFIX, self.server_name, self.tool_name
            ),
        }
    }

    fn priority(&self) -> i32 {
        MCP_HOOK_PRIORITY
    }

    fn name(&self) -> String {
        format!("McpToolExecuteHandler({})", self.server_name)
    }
}

