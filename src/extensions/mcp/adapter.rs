#[async_trait]
impl HookHandler for McpServerInitHandler {
    async fn handle(&self, _ctx: HookContext) -> HookResult {
        debug!(server_name = %self.server_name, "Initializing MCP server on agent init");

        let server_config = if self.config_path.as_os_str().is_empty() {
            // ADR-024: Load from embedded mcp_servers in unified manifest
            match &self.mcp_servers {
                Some(mcp_servers) => {
                    match Self::load_server_config_from_json(mcp_servers, &self.server_name).await {
                        Ok(config) => config,
                        Err(e) => {
                            warn!(server_name = %self.server_name, error = %e, "Failed to load MCP server config from unified manifest");
                            return HookResult::Continue(
                                crate::extensions::framework::types::HookOutput::Unit,
                            );
                        }
                    }
                }
                None => {
                    warn!(server_name = %self.server_name, "No config_path or mcp_servers available for MCP server");
                    return HookResult::Continue(
                        crate::extensions::framework::types::HookOutput::Unit,
                    );
                }
            }
        } else {
            match McpAdapter::load_server_config(&self.config_path).await {
                Ok(config) => config,
                Err(e) => {
                    warn!(server_name = %self.server_name, error = %e, "Failed to load MCP server config");
                    return HookResult::Continue(
                        crate::extensions::framework::types::HookOutput::Unit,
                    );
                }
            }
        };

        let manager = self.manager.read().await;
        let exists = manager.get_server_state(&self.server_name).await.is_ok();
        drop(manager);

        if !exists {
            let manager = self.manager.read().await;
            if let Err(e) = manager.add_server_config(server_config).await {
                warn!(server_name = %self.server_name, error = %e, "Failed to add MCP server config");
                return HookResult::Continue(crate::extensions::framework::types::HookOutput::Unit);
            }
            info!(server_name = %self.server_name, "Added MCP server config to manager");
        }

        // Do NOT auto-start the server here.  Starting the server on every
        // AgentInit hook (which fires for *any* agent, not just the one that
        // enabled this extension) causes `ext start` to fail with "Runtime
        // already exists".  The server will be started on demand by
        // `ext start` or `McpToolProxy::call_with_auto_start()`.
        let manager = self.manager.read().await;
        let _is_running = match manager.get_server_state(&self.server_name).await {
            Ok(state) if state.running => true,
            _ => false,
        };
        drop(manager);

        // Register tools with unified registry so the agent can use them.
        // Registration does not require the server to be running; tool
        // metadata was discovered when the extension was installed.
        if let Some(core) = crate::extensions::framework::core::global_core() {
            let adapter = McpAdapter::new(self.manager.clone());
            match adapter
                .register_server_tools(
                    &core,
                    &self.server_name,
                    crate::subject::PrincipalId::system(),
                )
                .await
            {
                Ok(count) => {
                    info!(server_name = %self.server_name, count = count, "Registered MCP tools with unified registry");
                }
                Err(e) => {
                    warn!(server_name = %self.server_name, error = %e, "Failed to register MCP tools");
                }
            }
        } else {
            warn!("No global ExtensionCore available for MCP tool registration");
        }

        HookResult::Continue(crate::extensions::framework::types::HookOutput::Unit)
    }

    fn hook_point(&self) -> HookPoint {
        HookPoint::AgentInit
    }

    fn priority(&self) -> i32 {
        MCP_HOOK_PRIORITY
    }

    fn name(&self) -> String {
        format!("McpServerInitHandler({})", self.server_name)
    }
}

impl McpServerInitHandler {
    /// Load server config from embedded mcp_servers JSON (ADR-024 unified manifest).
    async fn load_server_config_from_json(
        mcp_servers: &serde_json::Value,
        server_name: &str,
    ) -> Result<crate::extensions::mcp::protocol::config::McpServerConfig> {
        let servers_map = mcp_servers
            .as_object()
            .ok_or_else(|| anyhow::anyhow!("mcp_servers must be an object"))?;

        let server_value = servers_map
            .get(server_name)
            .ok_or_else(|| anyhow::anyhow!("Server '{}' not found in mcp_servers", server_name))?;

        // Inject the server name into the JSON value before deserializing
        let mut server_json = server_value.clone();
        if let Some(obj) = server_json.as_object_mut() {
            if !obj.contains_key("name") {
                obj.insert("name".to_string(), serde_json::json!(server_name));
            }
        }

        let server_config: crate::extensions::mcp::protocol::config::McpServerConfig =
            serde_json::from_value(server_json).with_context(|| {
                format!("Failed to parse mcp_servers config for '{server_name}'")
            })?;

        Ok(server_config)
    }
}

