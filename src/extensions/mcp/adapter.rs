/// Load MCP servers from directory using the adapter
pub async fn load_servers_from_directory(
    path: &Path,
    manager: Arc<RwLock<crate::extensions::mcp::protocol::manager::McpManager>>,
) -> Vec<DiscoveredMcpServer> {
    let adapter = McpAdapter::new(manager);
    adapter.discover_servers(path).await
}

/// Load and register MCP servers with an ExtensionCore
pub async fn load_and_register_servers(
    core: &crate::extensions::ExtensionCore,
    servers_dir: impl AsRef<Path>,
    manager: Arc<RwLock<crate::extensions::mcp::protocol::manager::McpManager>>,
) -> Result<usize> {
    let adapter = McpAdapter::new(manager.clone());
    let servers = adapter.discover_servers(servers_dir.as_ref()).await;
    let mut total_tools = 0;
    for server in servers {
        // Register lifecycle hooks (init/shutdown)
        let extension_id = ExtensionId::new(&server.manifest.id.0);
        let bindings = adapter.resolve_hooks(&server.manifest);
        for binding in bindings {
            let handler = binding.handler_factory.create(server.manifest.clone());
            let handler_arc: Arc<dyn HookHandler> = handler.into();
            core.register_hook(binding.point, handler_arc, &extension_id)
                .await?;
        }
        // Register tools via unified registry
        if let Some(config_path) = server.manifest.get("config_path").and_then(|v| v.as_str()) {
            let _ = adapter.ensure_server_config(config_path).await;
        }
        match adapter.register_server_tools(core, &server.manifest.name).await {
            Ok(n) => total_tools += n,
            Err(e) => warn!(server = %server.manifest.name, error = %e, "Failed to register MCP tools"),
        }
    }
    Ok(total_tools)
}

